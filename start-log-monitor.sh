#!/bin/bash

# 启动日志监控（在新终端窗口）

LOG_FILE="webhook.log"

# 确保日志文件存在
touch "$LOG_FILE"

echo "=================================================="
echo "📊 Webhook 日志监控"
echo "=================================================="
echo ""
echo "日志文件: $(pwd)/$LOG_FILE"
echo ""
echo "实时显示日志中..."
echo "（在 Line 中发送消息后，日志会实时显示）"
echo ""
echo "按 Ctrl+C 退出监控"
echo "=================================================="
echo ""

# 显示现有日志（如果有）
if [ -s "$LOG_FILE" ]; then
  echo "📋 现有日志（最后 20 行）:"
  echo "----------------------------------------"
  tail -20 "$LOG_FILE"
  echo "----------------------------------------"
  echo ""
  echo "等待新日志..."
  echo ""
fi

# 实时监控
tail -f "$LOG_FILE"

